////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// @enum Result: Defines the two results of a GW2API query: Success & Failure. Success returns generic type T, Failure returns generic error type U.  //
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
enum Result<T, U> where U: Error {
    case success(T)
    case failure(U)
}
